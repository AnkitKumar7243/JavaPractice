public class fifth_incrementdecrement {
    public static void main(String[] args) {
        int x=5;
        System.out.println(x);
        x++;//x++ ka matlab hai x= x+1;
        System.out.println(x);
        x--;//x--ka matlab hai x= x-1;
        System.out.println(x);
        //x++ is "use x and then increment"
        //++x is "increment x and then use "
        //x-- is post increment operator.
        //--x is pre increment operator.
        
    }
    
}
